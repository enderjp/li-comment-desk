function getNestedValue(tree: TranslationTree, key: string): string | undefined {
  return key.split('.').reduce<string | TranslationTree | undefined>((current, part) => {
    if (!current || typeof current === 'string') {
      return undefined;
    }

    return current[part];
  }, tree) as string | undefined;
}

export function translate(
  language: Language,
  key: string,
  replacements?: Record<string, string>
): string {
  const fallback = getNestedValue(translations.es, key);
  const value = getNestedValue(translations[language], key) ?? fallback ?? key;

  if (typeof value !== 'string') {
    return key;
  }

  if (!replacements) {
    return value;
  }

  return Object.entries(replacements).reduce(
    (message, [token, replacement]) => message.split(`{${token}}`).join(replacement),
    value
  );
}
